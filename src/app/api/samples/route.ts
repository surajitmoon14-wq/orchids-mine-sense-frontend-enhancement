export async function GET() {
  try {
    const filePath = path.join(process.cwd(), 'public', 'sonar_samples.txt');
    let lines: string[] = [];
    
    try {
      const content = fs.readFileSync(filePath, 'utf-8');
      lines = content.split('\n').filter(l => l.trim() !== '');
    } catch {
      lines = VALID_SAMPLES;
    }
    
    if (lines.length === 0) {
      lines = VALID_SAMPLES;
    }
    
    const randomLine = lines[Math.floor(Math.random() * lines.length)];
    const parts = randomLine.split(',');
    
    if (parts.length < 61) {
      return NextResponse.json(
        { error: 'Invalid sample format' },
        { status: 500 }
      );
    }
    
    const features = parts.slice(0, 60).map(Number);
    const actualLabel = parts[60]?.trim() || '';
    
    for (let i = 0; i < 60; i++) {
      if (isNaN(features[i]) || features[i] < 0 || features[i] > 1) {
        return NextResponse.json(
          { error: 'Invalid feature values in sample' },
          { status: 500 }
        );
      }
    }
    
    return NextResponse.json({ 
      features,
      actualLabel: actualLabel === 'R' ? 'Rock' : actualLabel === 'M' ? 'Mine' : 'Unknown',
      isFromDataset: true
    });
  } catch (error) {
    console.error('Error loading sample:', error);
    return NextResponse.json(
      { error: 'Failed to load sample' },
      { status: 500 }
    );
  }
}
